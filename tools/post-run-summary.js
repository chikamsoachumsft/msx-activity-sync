#!/usr/bin/env node
// post-run-summary.js
// Picks the most recent report from ./reports/, copies it to the reports-repo,
// regenerates README.md ("latest-run") with rolled-up status, and pushes.
//
// Usage: node tools/post-run-summary.js [<reports-repo-path>]
//   defaults to %USERPROFILE%/.activity-sync/reports-repo

import { existsSync, readdirSync, readFileSync, writeFileSync, copyFileSync, mkdirSync } from 'node:fs';
import { join, basename } from 'node:path';
import { homedir } from 'node:os';
import { execSync } from 'node:child_process';

const SOURCE_DIR = join(process.cwd(), 'reports');
const REPO_DIR = process.argv[2] || join(homedir(), '.activity-sync', 'reports-repo');

if (!existsSync(SOURCE_DIR)) {
  console.error(`[summary] No reports dir at ${SOURCE_DIR} — nothing to publish.`);
  process.exit(0);
}
if (!existsSync(REPO_DIR)) {
  console.error(`[summary] Reports-repo not initialized at ${REPO_DIR}. Run setup.ps1.`);
  process.exit(0); // soft-fail so sync isn't marked failed
}

// 1. Find the most recent sync_*.md report
const reports = readdirSync(SOURCE_DIR)
  .filter(f => /^\d{4}-\d{2}-\d{2}T.+_sync_.+\.md$/.test(f))
  .sort()
  .reverse();

if (reports.length === 0) {
  console.error('[summary] No sync reports found — skipping.');
  process.exit(0);
}

const latestName = reports[0];
const latestPath = join(SOURCE_DIR, latestName);
const latestContent = readFileSync(latestPath, 'utf8');

// 2. Copy report into the repo
const destReportsDir = join(REPO_DIR, 'reports');
if (!existsSync(destReportsDir)) mkdirSync(destReportsDir, { recursive: true });
const destReport = join(destReportsDir, latestName);
copyFileSync(latestPath, destReport);

// 3. Extract metrics from the report
const extract = (re, def = '?') => {
  const m = latestContent.match(re);
  return m ? m[1].trim() : def;
};
const created = extract(/\|\s*Tasks Created\s*\|\s*(\d+)\s*\|/i, '0');
const skipped = extract(/\|\s*Meetings Skipped\s*\|\s*(\d+)\s*\|/i, '0');
const failed = extract(/\|\s*Failures\s*\|\s*(\d+)\s*\|/i, '0');
const followUp = extract(/\|\s*Follow-Up Required\s*\|\s*(\d+)\s*\|/i, '0');
const range = extract(/\*\*Range\*\*:\s*(\S+\s*→\s*\S+)/i);
const ts = extract(/\*\*Date\*\*:\s*(\S+)/i);

// Pull the master follow-up table and human-action section verbatim if present
const humanAction = (() => {
  const m = latestContent.match(/##\s+Human Action Required([\s\S]*?)(?=\n##\s|$)/i);
  return m ? m[1].trim() : '_None_';
})();
const masterFollowUp = (() => {
  const m = latestContent.match(/##\s+Master Follow-Up List[^\n]*\n([\s\S]*?)(?=\n##\s|$)/i);
  return m ? m[1].trim() : '';
})();

const status = Number(failed) > 0 ? '❌ Failed' : Number(created) > 0 ? '✅ Success' : '⚪ No-op';
const badge = Number(failed) > 0
  ? '![status](https://img.shields.io/badge/last%20run-failed-red)'
  : '![status](https://img.shields.io/badge/last%20run-success-brightgreen)';

// 4. Build README.md
const reportLink = `./reports/${latestName}`;
const readme = `# Activity Sync Logs

${badge}

> Personal CRM activity sync logs. Auto-pushed after each scheduled run on the local machine.

## Latest Run — ${status}

| | |
|---|---|
| **Timestamp** | ${ts} |
| **Range** | ${range} |
| **Tasks created** | ${created} |
| **Meetings skipped** | ${skipped} |
| **Failures** | ${failed} |
| **Follow-up required** | ${followUp} |

[Full report →](${reportLink})

## Human Action Required

${humanAction}

## Master Follow-Up List

${masterFollowUp || '_None tracked yet._'}

---
_Generated by \`tools/post-run-summary.js\` at ${new Date().toISOString()}_
`;

writeFileSync(join(REPO_DIR, 'README.md'), readme, 'utf8');
console.log(`[summary] Wrote README + ${basename(destReport)}`);

// 5. Commit + push
const sh = (cmd) => execSync(cmd, { cwd: REPO_DIR, stdio: 'pipe' }).toString().trim();
try {
  sh('git add .');
  // Only commit if there are staged changes
  try { sh('git diff --cached --quiet'); console.log('[summary] No changes — nothing to commit.'); process.exit(0); }
  catch { /* there are changes */ }
  sh(`git commit -m "sync ${ts || new Date().toISOString()}: ${status} (${created} created, ${failed} failed)"`);
  sh('git push --quiet');
  console.log('[summary] Pushed to reports-repo.');
} catch (err) {
  console.error('[summary] Git operation failed:', err.message);
  process.exit(0); // don't fail the sync
}
